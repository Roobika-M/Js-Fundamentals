//  + operator
// + is used for string concatenation, so if one value is string and other one is number, it'll be converted as string

let str1 = "" + 1 // output = "1"
let str2 = "hello" + 1 // output = "hello1"
let str3 = true + "1" // output = "true1"

// - , *, / operator
// these values works on numbers, so it'll be converted to numbers

let num1 = "5" - 1 // output = 4
let num2 = "5" * 2 // output = 10
let num3 = "5" / 2 // output = 2.5
let num4 = "" -  1 // output = -1